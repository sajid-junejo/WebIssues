    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("Edit Description".equals(this.getTitle())) {
            String originalText = jTextPane1.getText();
            String modifiedText = originalText.replace("\n", "\\n");
            System.out.println(modifiedText);
            issueDao.editDescription(HomeFrame.IssueID, modifiedText);
            dispose();
        } else if ("Edit Comment".equals(this.getTitle())) {
            String originalText = jTextPane1.getText();
            String modifiedText = originalText.replace("\n", "\\n");
            System.out.println(modifiedText);
            String text = name.getText();
            String[] parts = text.split("#");
            String numberWithoutHash = parts[1].trim(); 
            numberWithoutHash = numberWithoutHash.replaceAll("\\<.*?\\>", "");
            int commentId = Integer.parseInt(numberWithoutHash);
            System.out.println(commentId);
            issueDao.editComment(commentId, modifiedText);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

