    public List<String> getAllColumns() {
    List<String> selectedColumns = new ArrayList<>();
    List<JCheckBox> checkBoxList = Arrays.asList(jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4,
            jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10);
    for (JCheckBox checkBox : checkBoxList) {
        checkBox.setSelected(true);
        if (checkBox.isSelected()) {
        //    checkBox.setSelected(true);
            selectedColumns.add(columnNames.get(checkBoxList.indexOf(checkBox)));            
        } 
    }
    return selectedColumns;
    }
    
    
    public List<String> getSelectedColumns(){
       List<String> updatedColumns = new ArrayList<>();
    List<JCheckBox> checkBoxList = Arrays.asList(jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4,
            jCheckBox5, jCheckBox6, jCheckBox7, jCheckBox8, jCheckBox9, jCheckBox10);
    for (JCheckBox checkBox : checkBoxList) {
        //checkBox.setSelected(true);
        if (checkBox.isSelected()) {
            updatedColumns.add(columnNames.get(checkBoxList.indexOf(checkBox)));                 
        } 
        else
        {
            checkBox.setSelected(false);
        }
    }
        
    return updatedColumns;        
    }

